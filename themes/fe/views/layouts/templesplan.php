	<li><h2>Padas </h2><p> The central squares are dedicated to Brahman. The first concentric square surrounding the Brahma Pada is called Devika Pada, dedicated to good or Devas. The concentric square surrounding the Devika Pada is the Manusha Pada dedicated to man. The last concentric square surrounding the Manusha Pada is the Paisachika Pada representing evil or the Asuras. In large Indian temples the three Padas are decorated with murals, paintings and carvings.</p>

</li>

	<li><h2>Garbhagriha  </h2><p>At the centre of the Brahma Padas is the Garbhagriha, a small windowless enclosed space in a square shape which represents the universal spirit or Purusha. This is the sanctum sanctorum wherein an idol or Murthi may be placed. This is the main deity of the temple.</p>

</li>

	<li><h2>Shikara or Vimanam  </h2><p>Directly above and rising above the Garbhagriha or Brahma Padas is the spire or tower structure called Shikara in the north and Vimanam in the South. This is the defining structure in a Hindu temple and is normally the most visible part of the temple.</p>

</li>

