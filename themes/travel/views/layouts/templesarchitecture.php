<h2>The Dravidian architecture</h2>

 <p>This style of architecture is commonly found in temples in South India especially in Tamilnadu.</p>

 <p>Dravidian architecture features the square sanctum sanctorum with a multi-storied Vimanam. The vimanam is the tower that covers the sanctum sanctorum or garbha griha.</p>

 <p> The Mandapas or halls usually pillared are always found before the doors that lead to the garbha grihah.</p>

 <p> Pillared halls are found in all large south Indian temples and these were used for many purposes including dance and music competitions.</p>

<p>The Gopurams or gate towers dominate the skyline and are the most distinguishable feature of Dravidian architecture. Examples of Dravidian architecture are found in <a href="http://www.templeadvisor.com/temples/info/10028">Brihadeeshwara temple</a>  at Tanjore and <a href="http://www.templeadvisor.com/temples/info/10047">Airavatesvara temple</a>  at Kumbakonam.</p>

<h2>Vesara architecture</h2>

<p>These are commonly found in Hindu temples in the Deccan - modern Karnataka and Maharashtra. </p>

            <p> Chalukya and Hoysala temples are known for their lower profile and more intricate carvings compared to the Dravidian or Nagara styles.</p>

             <p>The distinguishing feature of the Chalukya architecture is architectural articulation which refers to the projections, recesses and decorations on the outer walls. The intricate carvings were another feature. Examples of famous Indian temples using this style are <a href="http://www.templeadvisor.com/temples/info/10342">Chennakesava temple</a>  at Belur and <a href="http://www.templeadvisor.com/temples/info/10343">Hoysaleswara temple</a>  at Halebeedu.</p>

